  fpr_gnb, tpr_gnb, thresholds_gnb = roc_curve(y_train_digit[i-1], y_scores_gnb)
  fpr_log_reg, tpr_log_reg, thresholds_log_reg = roc_curve(y_train_digit[i-1], y_scores_log_reg)
